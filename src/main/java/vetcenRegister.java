    private void vetRegister(String password, String name, String address, String email, String phno) {
         Connection dbconn=ConnectionProvider.getCon();
        try {
            PreparedStatement st=(PreparedStatement)dbconn.prepareStatement("insert into vetcentre(cname,centemail,cenphone,caddress,password) values(?,?,?,?,?)");
            
            
            st.setString(1, name);
          
            st.setString(2, email);
            st.setString(3, phno);
            st.setString(4, address);
            st.setString(5,password);
          
            int res=st.executeUpdate();
            JOptionPane.showMessageDialog(this,"Veterinary centre data inserted Successfully","Success",JOptionPane.INFORMATION_MESSAGE);
            PreparedStatement sti=dbconn.prepareStatement("select centid from vetcentre where cname=?");
            sti.setString(1,name);
            ResultSet rs = sti.executeQuery();
            while (rs.next ( ))
                {
                   String category = rs.getString("centid");
                   jVetid.setText(category);
                }
            
            
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,ex);
        }
        
    }
        
