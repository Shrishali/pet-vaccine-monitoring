    private void jAddressMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jAddressMouseClicked
        // TODO add your handling code here:
        String regex="^(?=.*[0-9])"
                       + "(?=.*[a-z])(?=.*[A-Z])"

                       + "(?=.*[@#$%&*])"

                       + "(?=\\S+$).{8,25}$"; 

        Pattern p=Pattern.compile(regex);
        Matcher match=p.matcher(jPasswordField1.getText());
        if(!match.matches()){
            JOptionPane.showMessageDialog(null,"Password must contain atleast 8 character,one uppercase,lowercase,digit and special character(@,#,%,*,&)");
            
        }
        
        
    }//GEN-LAST:event_jAddressMouseClicked

    private void jPhonenoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPhonenoKeyPressed
        // TODO add your handling code here:
        String phoneno=jPhoneno.getText();
        int length=phoneno.length();
        char c=evt.getKeyChar();
        if(evt.getKeyChar()>='0'&& evt.getKeyChar()<='9')
        {
            if(length<10)
            {
                jPhoneno.setEditable(true);
                
            }
            else
            {
                jPhoneno.setEditable(false);
            }
        }
        else{
            if(evt.getExtendedKeyCode()==KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode()==KeyEvent.VK_DELETE)
            {
                jPhoneno.setEditable(true);
            }
            else{
                jPhoneno.setEditable(false);
            }
        }
                
    }//GEN-LAST:event_jPhonenoKeyPressed

