    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int i=jTable1.getSelectedRow();
        DefaultTableModel tb1Model=(DefaultTableModel)jTable1.getModel();
        if(i>=0)
        {
            tb1Model.setValueAt(jAval.getText(),i,3);
            String aval=jAval.getText();
            String centid=jTextField1.getText();
            try{
            Connection dbconn=ConnectionProvider.getCon();
            Statement st=dbconn.createStatement();
            st.executeUpdate("update addvaccine set availability="+aval+" where centid="+centid+"");
            JOptionPane.showMessageDialog(null,"Successfully Updated");
          
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
                
            }

        }
        else
                {
                     JOptionPane.showMessageDialog(null, "error");
                }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jAvalKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jAvalKeyPressed
        // TODO add your handling code here:
        char c=evt.getKeyChar();
        if(!(Character.isDigit(c)|| (c==KeyEvent.VK_BACK_SPACE)||(c==KeyEvent.VK_DELETE)))
        {
            jAval.setEditable(false);
            jLabel7.setText("Enter Numeric Value only");
            
            
        }
        else
        {
            jAval.setEditable(true);
        }
    }//GEN-LAST:event_jAvalKeyPressed

