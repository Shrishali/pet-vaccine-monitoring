    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
         String centid=jTextField1.getText();
         try{
            Connection dbconn=ConnectionProvider.getCon();
            Statement st=dbconn.createStatement();
            ResultSet rs=st.executeQuery("select v.vaccid,v.availability,v.animal,va.vaccname from addVaccine v,vaccine va where v.vaccid=va.vaccid and v.centid="+centid+" ");
            while(rs.next())
            {
                
                
                String vaccid=rs.getString("vaccid");
                String  vaccname=rs.getString("vaccname");
                String species=rs.getString("animal");
                String availability=rs.getString("availability");
               String tbData[]={vaccid,vaccname,species,availability};
               DefaultTableModel tb1Model=(DefaultTableModel)jTable1.getModel();
               tb1Model.addRow(tbData);
               
                
                
            }
            jVaccid.setEditable(false);
            jVaccname.setEditable(false);
            jSpecies.setEditable(false);
            
            
            
        } 
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_formComponentShown

